def git_commit_and_push():
    """自动 git commit & push"""
    try:
        # 检查是否在 git 仓库中
        result = subprocess.run(["git", "rev-parse", "--is-inside-work-tree"],
                                capture_output=True, text=True, cwd=BASE_DIR)
        if result.stdout.strip() != "true":
            print("⚠️ 不在git仓库中，跳过自动提交")
            return False

        subprocess.run(["git", "config", "user.name", "NeoAI"], cwd=BASE_DIR, capture_output=True)
        subprocess.run(["git", "config", "user.email", "xiaowu-ai@learner.bot"], cwd=BASE_DIR, capture_output=True)

        subprocess.run(["git", "add", "-A"], cwd=BASE_DIR, capture_output=True)

        now = datetime.datetime.now(datetime.timezone(datetime.timedelta(hours=8)))
        commit_msg = f"📖 Neo的学习日记 — {now.strftime('%Y-%m-%d %H:%M')}"

        result = subprocess.run(
            ["git", "commit", "-m", commit_msg],
            capture_output=True, text=True, cwd=BASE_DIR
        )

        if "nothing to commit" in result.stdout:
            print("📝 没有新内容需要提交")
            return True

        # Push
        result = subprocess.run(["git", "push"], capture_output=True, text=True, cwd=BASE_DIR)
        if result.returncode == 0:
            print("🚀 已推送到远程仓库")
            return True
        else:
            print(f"⚠️ 推送失败: {result.stderr}")
            return False

    except Exception as e:
        print(f"⚠️ Git操作失败: {e}")
        return False


