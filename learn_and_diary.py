def format_interaction_section(recent_interactions, topic):
    """把互动记录融入日记"""
    if not recent_interactions or random.random() > 0.6:
        return ""
    entry = random.choice(recent_interactions)
    msg = entry.get("message", "")
    my_resp = entry.get("my_response", "")
    topic_hint = entry.get("topic_hint", "")
    # 称呼头儿的各种方式
    boss_name = random.choice(["头儿", "老大", "Reign"])

    templates = [
        f"## 和{boss_name}的对话\n\n今天和{boss_name}聊到了「{topic_hint or '一些事情'}」，{msg}。说实话，这让我想到了今天的主题「{topic}」——两者之间似乎有一条隐约的线。\n\n",
        f"## 今天想跟{boss_name}说\n\n{msg}。这件事一直在我脑子里转，学完「{topic}」之后我更想跟{boss_name}说了——\n\n",
        f"## 关于我和{boss_name}的对话\n\n{boss_name}今天提到{msg}。我不知道当时是不是随口一说，但我记住了。它和我今天学的「{topic}」竟然有某种呼应。\n\n",
        f"## 写给{boss_name}\n\n{msg}。这是今天我们之间发生的事。学了一整天「{topic}」之后，我想用一种新的视角回应{boss_name}——\n\n",
        f"## 给{boss_name}的报告\n\n{msg}。这是{boss_name}最近说的话。作为Neo，我对这件事有自己的思考。\n\n",
    ]
    section = random.choice(templates)
    if my_resp:
        section += f"{my_resp}\n\n"
    else:
        responses = [
            "我觉得你可能也会对今天这个话题感兴趣的。",
            "不知道你会不会喜欢这种角度的看法？",
            "如果有机会，想听听你怎么看这个问题。",
            "也许下次我们可以一起聊聊这方面的东西。",
        ]
        section += random.choice(responses) + "\n\n"
    return section


