def search_duckduckgo(topic):
    """用 DuckDuckGo 即时回答 API 搜索"""
    snippets = []
    try:
        import requests
        url = f"https://api.duckduckgo.com/?q={topic}+科普+有趣&format=json&no_html=1"
        resp = requests.get(url, timeout=10)
        data = resp.json()
        if data.get("AbstractText"):
            snippets.append(("ddg", data["AbstractText"]))
        for t in data.get("RelatedTopics", [])[:3]:
            if isinstance(t, dict) and t.get("Text"):
                snippets.append(("ddg_related", t["Text"]))
    except Exception as e:
        print(f"  DuckDuckGo查询失败: {e}")
    return snippets


def extract_sentences(text, min_len=15, max_count=8):
    """从文本中提取有意义的句子"""
    sentences = re.split(r'[。.！!？?\n]', text)
    result = []
    for s in sentences:
        s = s.strip()
        if min_len < len(s) < 300:
            # 过滤掉无意义内容
            if not any(skip in s for skip in ['Wikipedia', 'wiki', 'http', 'www', '导航', '搜索', '本条目', '翻譯', '维基']):
                result.append(s)
    return result[:max_count]


